package com.projectplus.estimation;

public class FunctionPointsDataStructure {
	int UDF;
	double VAF;
	int FP;
	public int getUDF() {
		return UDF;
	}
	public void setUDF(int uDF) {
		UDF = uDF;
	}
	public double getVAF() {
		return VAF;
	}
	public void setVAF(double vAF) {
		VAF = vAF;
	}
	public int getFP() {
		return FP;
	}
	public void setFP(int fP) {
		FP = fP;
	}
}
